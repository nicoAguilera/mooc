<h1>Editor de contenido</h1>

<h2>Haga click en el ícono de lápiz de la parte superior izquierda</h2>

<p>Esta página es una plantilla editable que puede utilizar como modelo para agregar el contenido de cada actividad del curso</p>

<p>Tan pronto como clickees el icono de lápiz la página sera editable y deberán aparecer dos nuevos elementos; el <strong>cuadro de herramientas</strong> y la <strong>barra de inspección</strong>. El cuadro de herramientas por defecto deberá aparecer en la esquina superior izquierda de la página, pero puede ser arrastrada al lugar que te resulte más cómodo. La barra de inspección deberá aparecer fija en la parte inferior de la página (Estará vacía hasta que un elemento de la página es seleccionado).</p>

<h3>Agregar y convertir contenido</h3>

<p>Existen dos formas de agregar nuevo contenido a la página; presionando la tecla enter mientras edita una sección de contenido o usando una de las herramientas en el cuadro de herramientas, por ejemplo para insertar una imagen, video, tabla, etc.</p>

<p>Algunas herramientas pueden convertir contenido, por ejemplo seleccionando un párrafo y haciendo click en la herramienta lista se convertirá en una nueva lista.</p>

<h3>Arrastrar y ubicar el contenido</h3>

<p>Cada bloque de contenido puede ser arrastrado a una nueva ubicación. Los elementos que no contienen texto como imágenes y videos pueden ser arrastrados haciendo click en el centro del elemento y manteniendo presionado moverlos a la ubicación deseada.</p>

<p>Cuando arrastra un elemento sobre otros, aparecerán flechas indicando donde se ubicara el contenido una vez que lo suelte. Todos los elementos pueden ser ubicados por encima o por debajo de otros, mientras que algunos elementos como imágenes y videos pueden ser ubicados a la izquierda o la derecha de otro contenido.</p>

<h3>Modificar el tamaño de imágenes y videos</h3>

<p>Para modificar el tamaño de una imagen o video, haga click y manteniendo presionado estire o comprima hasta lograr el tamaño deseado. Mientras cambia el tamaño, las dimensiones serán mostradas en la esquina superior derecha del elemento.</p>

<h3>Eliminar contenido</h3>

<p>Algunos elementos se eliminan automáticamente cuando se vacía su contenido, por ejemplo elementos de texto e ítems de lista se eliminaran a sí mismos cuando estén vacíos, tan pronto como pierdan el foco. Otros elementos como imágenes y videos pueden ser eliminados usando la herramienta de eliminación en el cuadro de herramientas o presionando la tecla suprimir luego de seleccionarlos con un click.</p>

<h3>Imágenes</h3>

<p>El editor provee una interfaz para subir imágenes, rotarlas y recortarlas antes de insertarlas en la página.</p>

<h3>Historial</h3>

<p>El editor soporta ilimitados deshacer/rehacer para la sesión de edición. Para realizar esto debe usar las herramientas deshacer/rehacer en el cuadro de herramientas.</p>